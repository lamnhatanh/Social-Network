    private void openProfileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openProfileButtonActionPerformed
        if (peopleTree.getLastSelectedPathComponent() != null) {
            String name = peopleTree.getLastSelectedPathComponent().toString();
            if (name != null) {
                Profile person = network.search(name);
                ProfilePanel userView = new ProfilePanel(network, person);
                userView.setVisible(true);
                this.dispose();
            } 
        }
        
        else
                JOptionPane.showMessageDialog(null, "Choose the name you want to open");
    }//GEN-LAST:event_openProfileButtonActionPerformed

    private void btnJoinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJoinActionPerformed

        Profile person = new Profile(txtName.getText(), null, txtStatus.getText());
        
        if (person.getName() != null && !person.getName().isEmpty()) {
            if (fileChooser != null && fileChooser.getSelectedFile() != null
                    && fileChooser.getSelectedFile().getPath() != null) {
                person.setImage(fileChooser.getSelectedFile().getPath());
            }
            boolean added = network.join(person);
            if (added) {
                JOptionPane.showMessageDialog(rootPane, "Person added", "Message", JOptionPane.INFORMATION_MESSAGE);
            } 
            
            else {
                JOptionPane.showMessageDialog(rootPane, "A person with same name already exists!", "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        else {
            JOptionPane.showMessageDialog(rootPane, "Name can not be empty!", "Message", JOptionPane.ERROR_MESSAGE);
        }

        txtName.setText("");
        txtStatus.setText("");
    }//GEN-LAST:event_btnJoinActionPerformed

    private void btnSelectImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectImageActionPerformed
        int value = fileChooser.showDialog(this, "Select an image");
        FileFilter filter = new FileNameExtensionFilter("JPEG file", "PNG", "jpeg");
        fileChooser.addChoosableFileFilter(filter);
        if (value == JFileChooser.APPROVE_OPTION) {
            final String absolutePath = fileChooser.getSelectedFile().getAbsolutePath();
            System.out.println(absolutePath);
            pnlImage.setLayout(new CardLayout());
            imagePanel = new ImagePanel(absolutePath);
            imagePanel.setPreferredSize(new Dimension(150, 150));
            pnlImage.setPreferredSize(new Dimension(150, 150));
            pnlImage.removeAll();
            pnlImage.add(imagePanel);
            pnlImage.revalidate();
            pnlImage.repaint();

        }
    }//GEN-LAST:event_btnSelectImageActionPerformed


