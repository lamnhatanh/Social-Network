    private void updateTreeComponent() {
        DefaultTreeModel model = (DefaultTreeModel) peopleTree.getModel();
        root.removeAllChildren();
        model.reload();
        for (Profile person : network.getPeople()) {
            DefaultMutableTreeNode namePerson = new DefaultMutableTreeNode(person.getName());
            model.insertNodeInto(namePerson, root, root.getChildCount());
        }
        peopleTree.setModel(new javax.swing.tree.DefaultTreeModel(root));

    }
